greet = "Hello, World!"
print(type(greet))  # Output: <class 'str'>
print(greet[1])     # Output: e

# Get the substring "Hello"
print(greet[0:5])

# Get the substring "World!"
print(greet[7:])

# Get the substring "World"
print(greet[7:12])

# Get every second character
print(greet[::2])

# Reverse the string
print(greet[::-1])
