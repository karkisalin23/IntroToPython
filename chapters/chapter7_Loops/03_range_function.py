#Range function in python

#range(start, stop, step-size) - step-size is usually not used with range()

#Loop through a range of numbers starting from 1 to 7
for i in range(8):
    # Print the current value of i
    print(i)

#Loop through a range of numbers starting from 1 to 7
for i in range(1, 8):
    # Print the current value of i
    print(i)

# Loop through a range of numbers starting from 1 to 7, with a step of 2
for i in range(1, 8, 2):
    # Print the current value of i
    print(i)
